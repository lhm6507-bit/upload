package com.hm.attr;

public class Luck {
		private String name;
		private int num;
		private int score;
		public String getName() {
			return name;
		}
		public void setName(String name) {
			this.name = name;
		}
		public int getNum() {
			return num;
		}
		public void setNum(int num) {
			this.num = num;
		}
		public int getScore() {
			return score;
		}
		public void setScore(int score) {
			this.score = score;
		}
		public Luck(String name, int num, int score) {
			super();
			this.name = name;
			this.num = num;
			this.score = score;
		}
		
		
		
		
}
